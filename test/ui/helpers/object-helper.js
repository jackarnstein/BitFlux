function Employee(id, name, title, office, startDate, endDate, email, phone, direct) {
    this.id = id;
    this.name = name;
    this.title = title;
    this.office = office;
    this.startDate = startDate;
    this.endDate = endDate;
    this.email = email;
    this.phone = phone;
    this.direct = direct;
}
